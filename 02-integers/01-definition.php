<?php

// десятичное целое число
$int1 = 100; 
echo $int1, '<br>';

// отрицательное число
$int2 = -25; 
echo $int2, '<br>';

// восьмеричное число (83)
$int3 = 0o123; 
echo $int3, '<br>';

// шестнадцатеричное число (1000)
$int4 = 0x3E8; 
echo $int4, '<br>';

// двоичное число (5)
$int5 = 0b101; 
echo $int5, '<br>';

// удобная запись больших чисел
$int6 = 1_000_000; 
echo $int6, '<br>';
